#!/usr/bin/env node
/* Builds assets/search-index.js by scanning all content HTML pages.
   Extracts: per-page entry, every qa-card (id=qN + question), every h2/h3 with an id.
   Pure regex parsing, no dependencies. Output is plain JS (works on file://). */
const fs = require("fs");
const path = require("path");

const SITE = path.resolve(__dirname, "..");
const PAGES = {
  "index.html": "首页 / 总览",
  "project-review.html": "学习路径 & 项目复盘",
  "math.html": "数学与统计基础",
  "programming.html": "编程 (Python / C++)",
  "factor.html": "因子与 Alpha 策略",
  "ml.html": "机器学习 / 深度学习",
  "strategies.html": "策略方向与经典论文",
  "frontier.html": "研究前沿",
  "resources.html": "工具 · 书单 · 资源",
};

function stripTags(s) {
  return s
    .replace(/<[^>]+>/g, "")
    .replace(/\$\$?[^$]*\$\$?/g, "") // drop math
    .replace(/&lt;/g, "<").replace(/&gt;/g, ">").replace(/&amp;/g, "&").replace(/&nbsp;/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

const entries = [];
for (const [file, pageTitle] of Object.entries(PAGES)) {
  const fp = path.join(SITE, file);
  if (!fs.existsSync(fp)) { console.warn("skip missing", file); continue; }
  const html = fs.readFileSync(fp, "utf8");

  // page-level entry
  const lead = (html.match(/<p class="page-lead">([\s\S]*?)<\/p>/) || [])[1];
  entries.push({
    title: pageTitle, href: file, anchor: "", pageTitle: "板块",
    keywords: "", snippet: lead ? stripTags(lead).slice(0, 60) : "",
  });

  // qa-cards: <div class="qa-card" id="qN"> ... <div class="qa-q">..<span class="qa-id">QN</span>QUESTION</div>
  const cardRe = /<div class="qa-card"[^>]*id="(q\d+)"[\s\S]*?<div class="qa-q">([\s\S]*?)<\/div>/g;
  let m;
  while ((m = cardRe.exec(html))) {
    const id = m[1];
    let q = stripTags(m[2]);
    const idNum = id.toUpperCase();
    if (!q.startsWith(idNum)) q = idNum + " " + q;
    entries.push({ title: q, href: file, anchor: id, pageTitle, keywords: id, snippet: "" });
  }

  // headings with id (h2/h3), skip ones already covered by qa-cards
  const headRe = /<(h2|h3)[^>]*id="([^"]+)"[^>]*>([\s\S]*?)<\/\1>/g;
  while ((m = headRe.exec(html))) {
    const anchor = m[2];
    if (/^q\d+$/.test(anchor)) continue;
    const title = stripTags(m[3]);
    if (!title) continue;
    entries.push({ title, href: file, anchor, pageTitle, keywords: "", snippet: "" });
  }
}

const out =
  "/* Auto-generated by scripts/build-search-index.js — do not edit by hand. */\n" +
  "window.SEARCH_INDEX = " + JSON.stringify(entries, null, 0) + ";\n";
fs.writeFileSync(path.join(SITE, "assets", "search-index.js"), out);
console.log("Wrote", entries.length, "search entries across",
  Object.keys(PAGES).filter((f) => fs.existsSync(path.join(SITE, f))).length, "pages.");
